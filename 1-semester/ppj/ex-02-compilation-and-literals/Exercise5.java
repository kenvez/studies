public class Exercise5 {
	public static void main(String[] args) {
		/*
			Wszystkie literały logiczne
		*/
			
		System.out.println(12);
		System.out.println(12l);
		System.out.println(12.12);
		System.out.println(12.12f);
		System.out.println(2 > 1);
		System.out.println(2 < 1);
		System.out.println('A');
		System.out.println("Hello PPJ!");
		// System.out.println(null);
		
		/*
			Literały tej samej liczby całkowitej przedstawionej w postaci dziesiętnej,
			oktalnej, heksadecymalnej i binarnej.
		*/
		
		System.out.println(85);
		System.out.println(0125);
		System.out.println(0x55);
		System.out.println(0b01010101);
		
		/*
			Przynajmniej dwa literały znakowe, z których przynajmniej jeden będzie zakodowany
			przy użyciu UTF16
		*/
		
		
		System.out.println('a');
		System.out.println('\u1671');
		
	}
}